package oogway.logic.exception;

/**
 * Represents an exception that is thrown by Oogway.
 */
public class OogwayException extends Exception {

    /**
     * Constructs a new OogwayException with the specified detail message.
     * @param message the detail message
     */
    public OogwayException(String message) {
        super(message);
    }
}
